#include "libft.h"

void	ft_striteri(char *s1, void (*f)(unsigned int, char *))
{
	int	i;

	if (s1 == 0)
		return ;
	i = 0;
	while (s1[i])
	{
		(*f)(i, s1 + i);
		i++;
	}
}
